--***************************************************************************
--                       Простые типы данных в T-SQL
--***************************************************************************

-- Создаем переменную с именем - 'а', типа Int, и присваиваем ей значение - 2.
DECLARE @A INT = 2;

-- Выводим на экран значение переменной @A.
PRINT @A;   -- 2

-- Переменной а присваиваем значение - 7.
SET @A = 7;

-- Выводим на экран значение переменной @A.
PRINT @A;   -- 7